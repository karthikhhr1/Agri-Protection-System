export function getRecommendedFrequency(animalIds: string[]): number {
  const animals = indianWildlifeFrequencies.filter(a => animalIds.includes(a.id));
  if (animals.length === 0) return 20;
  
  const avgOptimal = animals.reduce((sum, a) => sum + a.optimalFrequency, 0) / animals.length;
  return Math.round(avgOptimal);
}

export function getAnimalsByCategory(category: keyof typeof animalCategories): AnimalFrequencyData[] {
  return indianWildlifeFrequencies.filter(a => a.category === category);
}

export function getAnimalsByFrequencyRange(minHz: number, maxHz: number): AnimalFrequencyData[] {
  return indianWildlifeFrequencies.filter(a => 
    a.frequencyRange.min <= maxHz && a.frequencyRange.max >= minHz
  );
}
